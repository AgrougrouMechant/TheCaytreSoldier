void 		pause()
{
    int 	continuer = 1;
    SDL_Event event;
 
    while (continuer)
    {
        SDL_WaitEvent(&event);
        switch(event.type)
        {
            case SDL_QUIT:
                continuer = 0;
        }
    }
}


int 		*fill_map(char *path)
{
	int 	*map;
	char 	buffer[1152];
	int 	fd;
	int 	i;
	int 	index;

	i = 0;
	index = 0;
	map = malloc(sizeof(int) * 576);
	fd = open(path, O_RDONLY);
	read(fd, buffer, 1151);
	close(fd);
	buffer[1151] = '\0';
	while (buffer[i] != '\0')
	{
		if (buffer[i] != ' ' && buffer[i] != '\n')
		{
			map[index] = buffer[i] - 48;
			index++;
		}
		i++;
	}

	return (map);
}

